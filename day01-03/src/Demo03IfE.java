public class Demo03IfE {
    public static void main(String[] args) {
        int num = 3;
        if (num == 1){
            System.out.println("等于1");
        } else if (num == 2){
            System.out.println("等于2");
        } else {
            System.out.println("不等于1也不等于2");
        }
    }
}
