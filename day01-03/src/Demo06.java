public class Demo06 {
    public static void main(String[] args){
        int num1 = 10;
        System.out.println(num1);
        ++num1;
        System.out.println(num1);
        num1++;
        System.out.println(num1);
        System.out.println("===============================");
        //打印语句
        int num2 = 20;
        System.out.println(++num2);
        System.out.println(num2);

        int num3 = 30;
        System.out.println(num3++);
        System.out.println(num3);
    }

}
